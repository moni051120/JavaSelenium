package day1;

public class DataTypes {

    public static void main(String[] args) {

       // int upto 10 digits
        // short upto 5 digits
        // long upto 19 digits end with l
        // float for decimal use f
        // double holds larger number
        int age = 30;
        short pin = 12345;
        long cc = 12436547645464l;
        float per = 99.66f;
        double price = 24325455.99;
        boolean wfh = false;
        char ch = 's';

        System.out.println(age);
        System.out.println(pin);
        System.out.println(cc);
        System.out.println(per);
        System.out.println(price);
        System.out.println(wfh);
        System.out.println(ch);
    }
}
